const fs = require('node:fs');
const path = require('node:path');

const workspaceRoot = path.resolve(__dirname, '..');
const distRoot = path.join(workspaceRoot, 'dist');
const outputPath = path.join(workspaceRoot, 'deliverables', 'build-metrics.json');

function walkFiles(dir) {
  if (!fs.existsSync(dir)) {
    return [];
  }

  return fs.readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const absolutePath = path.join(dir, entry.name);

    return entry.isDirectory() ? walkFiles(absolutePath) : [absolutePath];
  });
}

function summarizeProject(projectName) {
  const projectRoot = path.join(distRoot, projectName);
  const files = walkFiles(projectRoot);
  const jsBytes = files
    .filter((file) => file.endsWith('.js') || file.endsWith('.mjs'))
    .reduce((total, file) => total + fs.statSync(file).size, 0);
  const cssBytes = files
    .filter((file) => file.endsWith('.css'))
    .reduce((total, file) => total + fs.statSync(file).size, 0);

  return {
    project: projectName,
    files: files.length,
    jsKb: Number((jsBytes / 1024).toFixed(2)),
    cssKb: Number((cssBytes / 1024).toFixed(2)),
  };
}

const projects = [
  'shell',
  'mfe-payments',
  'mfe-treasury',
  'mfe-auth',
  'mfe-compliance',
  'mfe-onboarding',
  'mfe-admin',
  'mfe-analytics',
  'docs',
];

const report = {
  capturedAt: new Date().toISOString(),
  source: 'dist artifacts generated by npm run ci:validate or npm run build:apps',
  projects: projects.map(summarizeProject),
};

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${JSON.stringify(report, null, 2)}\n`);
console.log(`Build metrics written to ${path.relative(workspaceRoot, outputPath)}`);
